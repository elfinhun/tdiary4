package com.tdiary.web.member.user.model;

public class UserInfo {
	
	private int user_seq;	
    private String user_id;
    private String password;
    private String name;
    private String phone_no;
    private String address;
    private String resident_no;
    private String job_code;
    private String system_use_code;
    private String create_date;
    private String update_date;
    
    
	public int getUser_seq() {
		return user_seq;
	}
	public void setUser_seq(int user_seq) {
		this.user_seq = user_seq;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone_no() {
		return phone_no;
	}
	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getResident_no() {
		return resident_no;
	}
	public void setResident_no(String resident_no) {
		this.resident_no = resident_no;
	}
	public String getJob_code() {
		return job_code;
	}
	public void setJob_code(String job_code) {
		this.job_code = job_code;
	}
	public String getSystem_use_code() {
		return system_use_code;
	}
	public void setSystem_use_code(String system_use_code) {
		this.system_use_code = system_use_code;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
    
    
    

}
